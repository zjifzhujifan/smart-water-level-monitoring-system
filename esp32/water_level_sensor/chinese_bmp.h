// ============ 绘制函数 ============

// 绘制单个16x16汉字
void drawHz(TFT_eSPI &tft, int x, int y, const uint8_t *bitmap, uint16_t color) {
  for (int row = 0; row < 16; row++) {
    uint8_t b1 = bitmap[row * 2];
    uint8_t b2 = bitmap[row * 2 + 1];
    for (int col = 0; col < 8; col++) {
      if (b1 & (0x80 >> col)) tft.drawPixel(x + col, y + row, color);
    }
    for (int col = 0; col < 8; col++) {
      if (b2 & (0x80 >> col)) tft.drawPixel(x + 8 + col, y + row, color);
    }
  }
}

